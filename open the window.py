import streamlit as st
import pandas as pd


# 제목
st.title("🌿 교실 환기 방식 분석 프로그램")

st.write(
    "학생 설문 데이터를 기반으로 환기 방식별 쾌적함, "
    "답답함, 집중도를 분석합니다."
)


# -----------------------------
# 데이터 입력
# -----------------------------

st.subheader("1. 설문 데이터")

data = pd.DataFrame({
    "환기방식": [
        "외부창문",
        "외부창문+문",
        "외부창문+복도창문"
    ],
    "답답함": [
        3.7,
        2.5,
        1.8
    ],
    "쾌적함": [
        2.8,
        3.7,
        4.5
    ],
    "집중도": [
        3.0,
        3.8,
        4.3
    ]
})


st.dataframe(data)


# -----------------------------
# 결측치 처리
# -----------------------------

st.subheader("2. 데이터 전처리")

st.write("결측치 개수")

st.write(data.isnull().sum())


data = data.dropna()


st.write("결측치 제거 후 데이터")

st.dataframe(data)



# -----------------------------
# 평균 비교
# -----------------------------

st.subheader("3. 환기 방식별 평균 비교")


st.write("쾌적함")

st.bar_chart(
    data.set_index("환기방식")["쾌적함"]
)


st.write("답답함")

st.bar_chart(
    data.set_index("환기방식")["답답함"]
)


st.write("집중도")

st.bar_chart(
    data.set_index("환기방식")["집중도"]
)



# -----------------------------
# 환기 효과 지수
# -----------------------------

st.subheader("4. 환기 효과 지수 계산")


data["환기효과지수"] = (
    data["쾌적함"]
    + data["집중도"]
    + (5-data["답답함"])
)


st.dataframe(
    data[["환기방식","환기효과지수"]]
)


st.bar_chart(
    data.set_index("환기방식")["환기효과지수"]
)



# -----------------------------
# 결과 추천
# -----------------------------

best = data.loc[
    data["환기효과지수"].idxmax(),
    "환기방식"
]


st.success(
    f"가장 효과적인 환기 방식: {best}"
)


st.write(
    """
    분석 결과, 환기 효과 지수가 가장 높은 방식은 
    학생들이 느끼는 쾌적함을 높이고 답답함을 줄이는 데
    가장 효과적인 방식으로 판단하였다.
    """
)
